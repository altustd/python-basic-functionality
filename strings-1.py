# Strings are sequences of characters enclosed in single quotes (' '), double quotes (" "), or triple quotes (''' ''' or """ """).
# They are immutable, meaning they cannot be changed after creation.

# Creating strings using different quotes
str1 = 'Hello, World!'
str2 = "Python is fun."
str3 = '''This is a multi-line
string example.'''      
str4 = """Another multi-line
string example."""  

print("String 1:", str1)
print("String 2:", str2)
print("String 3:", str3)        
print("String 4:", str4)


